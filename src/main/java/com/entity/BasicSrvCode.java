package com.entity;


public class BasicSrvCode {

  private long srvCodeId;
  private long srvCodeNo;
  private String srvCodeName;
  private long srvCodeGroupId;
  private long srvCodeLevel;
  private String srvCodeNote;


  public long getSrvCodeId() {
    return srvCodeId;
  }

  public void setSrvCodeId(long srvCodeId) {
    this.srvCodeId = srvCodeId;
  }


  public long getSrvCodeNo() {
    return srvCodeNo;
  }

  public void setSrvCodeNo(long srvCodeNo) {
    this.srvCodeNo = srvCodeNo;
  }


  public String getSrvCodeName() {
    return srvCodeName;
  }

  public void setSrvCodeName(String srvCodeName) {
    this.srvCodeName = srvCodeName;
  }


  public long getSrvCodeGroupId() {
    return srvCodeGroupId;
  }

  public void setSrvCodeGroupId(long srvCodeGroupId) {
    this.srvCodeGroupId = srvCodeGroupId;
  }


  public long getSrvCodeLevel() {
    return srvCodeLevel;
  }

  public void setSrvCodeLevel(long srvCodeLevel) {
    this.srvCodeLevel = srvCodeLevel;
  }


  public String getSrvCodeNote() {
    return srvCodeNote;
  }

  public void setSrvCodeNote(String srvCodeNote) {
    this.srvCodeNote = srvCodeNote;
  }

}
