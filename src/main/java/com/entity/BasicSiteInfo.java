package com.entity;


public class BasicSiteInfo {

  private long siteId;
  private String siteNo;
  private String siteName;
  private String siteAddress;
  private String siteZipCode;
  private String siteTelephone;
  private String siteNational;
  private String siteProvince;
  private String siteCity;
  private long siteType;


  public long getSiteId() {
    return siteId;
  }

  public void setSiteId(long siteId) {
    this.siteId = siteId;
  }


  public String getSiteNo() {
    return siteNo;
  }

  public void setSiteNo(String siteNo) {
    this.siteNo = siteNo;
  }


  public String getSiteName() {
    return siteName;
  }

  public void setSiteName(String siteName) {
    this.siteName = siteName;
  }


  public String getSiteAddress() {
    return siteAddress;
  }

  public void setSiteAddress(String siteAddress) {
    this.siteAddress = siteAddress;
  }


  public String getSiteZipCode() {
    return siteZipCode;
  }

  public void setSiteZipCode(String siteZipCode) {
    this.siteZipCode = siteZipCode;
  }


  public String getSiteTelephone() {
    return siteTelephone;
  }

  public void setSiteTelephone(String siteTelephone) {
    this.siteTelephone = siteTelephone;
  }


  public String getSiteNational() {
    return siteNational;
  }

  public void setSiteNational(String siteNational) {
    this.siteNational = siteNational;
  }


  public String getSiteProvince() {
    return siteProvince;
  }

  public void setSiteProvince(String siteProvince) {
    this.siteProvince = siteProvince;
  }


  public String getSiteCity() {
    return siteCity;
  }

  public void setSiteCity(String siteCity) {
    this.siteCity = siteCity;
  }


  public long getSiteType() {
    return siteType;
  }

  public void setSiteType(long siteType) {
    this.siteType = siteType;
  }

}
