package com.entity;


public class BasicVoice {

  private long voiceId;
  private String voiceName;
  private long voiceType;
  private String voiceModel;
  private long voiceSpeed;
  private long voiceMode;
  private long voiceVolumn;
  private String voiceNote;


  public long getVoiceId() {
    return voiceId;
  }

  public void setVoiceId(long voiceId) {
    this.voiceId = voiceId;
  }


  public String getVoiceName() {
    return voiceName;
  }

  public void setVoiceName(String voiceName) {
    this.voiceName = voiceName;
  }


  public long getVoiceType() {
    return voiceType;
  }

  public void setVoiceType(long voiceType) {
    this.voiceType = voiceType;
  }


  public String getVoiceModel() {
    return voiceModel;
  }

  public void setVoiceModel(String voiceModel) {
    this.voiceModel = voiceModel;
  }


  public long getVoiceSpeed() {
    return voiceSpeed;
  }

  public void setVoiceSpeed(long voiceSpeed) {
    this.voiceSpeed = voiceSpeed;
  }


  public long getVoiceMode() {
    return voiceMode;
  }

  public void setVoiceMode(long voiceMode) {
    this.voiceMode = voiceMode;
  }


  public long getVoiceVolumn() {
    return voiceVolumn;
  }

  public void setVoiceVolumn(long voiceVolumn) {
    this.voiceVolumn = voiceVolumn;
  }


  public String getVoiceNote() {
    return voiceNote;
  }

  public void setVoiceNote(String voiceNote) {
    this.voiceNote = voiceNote;
  }

}
